#include<iostream>
using namespace std;

int main() {

    int x = 10;

    x += 10;
    cout << " X += 10 é: " << x << endl;
    x -= 10;
    cout << " X -= 10 é: " << x << endl;
    x *= 10;
    cout << " X *= 10 é: " << x << endl;
    x /= 10;
    cout << " X /= 10 é: " << x << endl;
    x %= 10;
    cout << " X %= 10 é: " << x << endl;


    return 0;

}
